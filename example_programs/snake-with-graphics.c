int setupScreen(int height, int width) {
  __asm__(
    "89g 79g s"
    :
    : [r96] "=" (width), [r95] "=" (height)
  );
}

int setColor(int r, int g, int b) {
  __asm__(
    "89g 79g 69g f"
    :
    : [r96] "=" (b), [r95] "=" (g), [r94] "=" (r)
  );
}

int draw(void) {
  setColor(100, 100, 100);
  for (int i = 0; i < HEIGHT; i++) {
    for (int j = 0; j < WIDTH; j++) {
      __asm__(
        "89g 79g x"
        :
        : [r96] "=" (j), [r95] "=" (i)
      );
    }
  }

  setColor(0, 150, 0);
  for (int i = snakeStart; i != snakeEnd; i = (i+1)%MAX_TAIL_LENGTH) {
    __asm__(
      "89g 79g x"
      :
      : [r96] "=" (snakeTailX[i]), [r95] "=" (snakeTailY[i])
    );
  }

  setColor(0, 255, 0);
  __asm__(
    "89g 79g x"
    :
    : [r96] "=" (x), [r95] "=" (y)
  );

  setColor(255, 0, 0);
  __asm__(
    "89g 79g x"
    :
    : [r96] "=" (fruitx), [r95] "=" (fruity)
  );
}

int drawWin(void) {
  for (int i = 0; i < WIDTH; i++) {
    for (int j = 0; j < HEIGHT; j++) {
      int color = win_art[i+j*WIDTH];
      setColor((color/32) *36, ((color/4)%8) * 36, (color%4) * 85);
      __asm__(
        "89g 79g x"
        :
        : [r96] "=" (i), [r95] "=" (j)
      );
    }
  }
}

int drawLose(void) {
  for (int i = 0; i < WIDTH; i++) {
    for (int j = 0; j < HEIGHT; j++) {
      int color = lose_art[i+j*WIDTH];
      setColor((color/32) *36, ((color/4)%8) * 36, (color%4) * 85);
      __asm__(
        "89g 79g x"
        :
        : [r96] "=" (i), [r95] "=" (j)
      );
    }
  }
}

int main(void) {
  fruitx = 12;
  fruity = 10;

  setupScreen(HEIGHT, WIDTH);
  draw();

  int a = 0;

  while (1) {
    snakeTailX[snakeEnd] = x;
    snakeTailY[snakeEnd] = y;
    snakeEnd = (snakeEnd+1)%MAX_TAIL_LENGTH;

    switch (getchar()) {
      case 'a':
        x = (x-1);
        if (x < 0) {
          x = WIDTH-1;
        }
        break;
      case 'd':
        x = (x+1) % WIDTH;
        break;
      case 'w':
        y = (y-1);
        if (y < 0) {
          y = HEIGHT-1;
        }
        break;
      case 's':
        y = (y+1) % HEIGHT;
        break;
    }

    if (x == fruitx && y == fruity) {
      fruitx = ((fruity * 2) + 1) % WIDTH;
      fruity = ((fruitx * 3) - 1) % HEIGHT;
    } else if (a < 5) {
      a++;
    } else {
      snakeStart = (snakeStart+1)%MAX_TAIL_LENGTH;
    }

    for (int i = snakeStart; i != snakeEnd; i = (i+1)%MAX_TAIL_LENGTH) {
      if (snakeTailX[i] == x && snakeTailY[i] == y) {
        drawLose();
        return 1;
      }
    }

    if (snakeStart == snakeEnd) {
      drawWin();
      return 0;
    }

    draw();
  }
}
